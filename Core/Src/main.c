/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_GPDMA1_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_LPUART1_UART_Init();
  MX_UART4_Init();
  MX_USART2_UART_Init();
  MX_TIM3_Init();
  MX_TIM6_Init();
  /* USER CODE BEGIN 2 */

  maxModule.CHIP_ID = 0;
  maxModule.deviceReady = false;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

	  switch (State) {
		case IDLE:

			break;

		case MEASURE:
			if(!maxModule.deviceReady)
			{
				maxModule.CHIP_ID = MAX17048_connection(&hi2c1);

				if(maxModule.CHIP_ID)
				{
					tx.longitud = (uint16_t)snprintf( (char*)tx.bytes, sizeof(tx.bytes), "Device found with chip ID: %d\r\n",(uint8_t)maxModule.CHIP_ID);
					Send_UART(&tx);
					maxModule.deviceReady = true;
				}
				else
				{
					tx.longitud = (uint16_t)snprintf( (char*)tx.bytes, sizeof(tx.bytes), "No device found\r\n");
					Send_UART(&tx);
					maxModule.deviceReady = false;
				}
			}

			if(maxModule.deviceReady)
			{
				maxModule.SOC = MAX17048_getPercentage(&hi2c1);
				maxModule.VCELL = MAX17048_getVoltage(&hi2c1);
				maxModule.CRATE = MAX17048_getDisChargeRate(&hi2c1);

				State = SEND;
			}
			else
				State = IDLE;

			break;

		case SEND:
			tx.longitud = (uint16_t)snprintf( (char*)tx.bytes, sizeof(tx.bytes), "Battery Percentage: %.3f, VCELL: %.3f, Rate of Change Percentage: %.3f\r\n",(double)maxModule.SOC, (double)maxModule.VCELL, (double)maxModule.CRATE);
			Send_UART(&tx);

			HAL_Delay(1000);
			State = IDLE;

			break;

		default:
			break;
	}

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

